package Assignment_1;

public class Print_1_to_10
{
    public static void main(String[] args)
    {

       for(int i=1;i<=10;i++)
       {
           if(i%2==0)
           {
               System.out.println(i+"  EVEN number");
           }
           else
           {
               System.out.println(i+"  ODD number");
           }
       }
    }
}
