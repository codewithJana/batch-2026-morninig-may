package Conditional_Statement;

public class example_2 {
    static void main() {
      int day=2;
      switch (day){
          case 0:
              System.out.println("sunday");
              break;
          case 1:
              System.out.println("monday");
              break;
          default:
              System.out.println("no matches found");
        }
    }
}
